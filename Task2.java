package labWork.labAssignment1;

//Task 2 - Create a program in Java to declare, initialize and use local variables of all the primitive data types in Java.
public class Task2 {
    public static void main(String[] args) {
        byte byteVariable = 100;
        short shortVariable = 2300;
        char characterVariable = 'A';
        int integerVariable = 125465;
        long longVariable = 124569876;
        float floatVariable = 5.26f;
        double doubleVariable = 5.23546d;
        boolean booleanVariable = true;
        System.out.println("Byte Data Type = " + byteVariable);
        System.out.println("Short Data Type = " + shortVariable);
        System.out.println("Char Data Type = " + characterVariable);
        System.out.println("Int Data Type = " + integerVariable);
        System.out.println("Long Data Type = " + longVariable);
        System.out.println("Float Data Type = " + floatVariable);
        System.out.println("Double Data Type = " + doubleVariable);
        System.out.println("Boolean Data Type = " + booleanVariable);


    }
}
